import { NextRequest, NextResponse } from "next/server";
import { sendEmail } from "@/lib/email";
import { createEmailTemplate, createConfirmationContent } from "@/lib/email-templates";

const TO = process.env.OUVIDORIA_TO_EMAIL;
const FROM = process.env.OUVIDORIA_FROM_EMAIL;

function sanitizeFilename(name: string): string {
  return name.replace(/[^a-zA-Z0-9_.-]/g, "_").slice(0, 100);
}

export async function POST(req: NextRequest): Promise<NextResponse> {
  console.log('üîµ [OUVIDORIA API] Iniciando processamento...');
  
  try {
    console.log('üîë [OUVIDORIA API] RESEND_API_KEY:', process.env.RESEND_API_KEY ? 'Configurada' : 'N√ÉO CONFIGURADA');
    console.log('üìß [OUVIDORIA API] TO:', TO || 'N√ÉO CONFIGURADO');
    console.log('üì§ [OUVIDORIA API] FROM:', FROM || 'N√ÉO CONFIGURADO');
    
    if (!process.env.RESEND_API_KEY || !TO) {
      console.error('‚ùå [OUVIDORIA API] Configura√ß√£o ausente');
      return NextResponse.json({ error: "Configura√ß√£o de e‚Äëmail ausente." }, { status: 500 });
    }

    const contentType = req.headers.get("content-type") || "";
    if (!contentType.includes("multipart/form-data")) {
      return NextResponse.json({ error: "Envie como multipart/form-data." }, { status: 400 });
    }

    const form = await req.formData();

    // Honeypot check
    if ((form.get("website") as string)?.trim()) {
      return NextResponse.json({ ok: true });
    }

    const fields = Object.fromEntries(form.entries());

    const tipo = String(fields["tipo"] || "").trim();
    const relato = String(fields["relato"] || "").trim();
    const anonimo = !!fields["anonimo"];
    const email = String(fields["email"] || "").trim();
    const telefone = String(fields["telefone"] || "").trim();

    if (!tipo) return NextResponse.json({ error: "Tipo √© obrigat√≥rio." }, { status: 400 });
    if (!relato) return NextResponse.json({ error: "Relato √© obrigat√≥rio." }, { status: 400 });
    if (!anonimo && !email && !telefone) {
      return NextResponse.json({ error: "Informe e‚Äëmail ou telefone (ou marque anonimato)." }, { status: 400 });
    }

    const attachments: { filename: string; content: string }[] = [];
    const anexos = form.getAll("anexos");
    for (const file of anexos) {
      if (!(file instanceof File)) continue;
      if (file.size === 0) continue; // Pular arquivos vazios
      if (attachments.length >= 5) break;
      if (file.size > 5 * 1024 * 1024) continue;
      const buf = Buffer.from(await file.arrayBuffer());
      if (buf.length === 0) continue; // Pular buffers vazios
      attachments.push({ filename: sanitizeFilename(file.name), content: buf.toString("base64") });
    }

    // Gerar protocolo
    const protocol = `OUV-${new Date().getFullYear()}-${Math.floor(Math.random() * 999999).toString().padStart(6, "0")}`;

    // Template para equipe interna
    const internalContent = `
      <div class="section">
        <h2>Informa√ß√µes da Manifesta√ß√£o</h2>
        <div class="field-group">
          <div class="field">
            <span class="field-label">Tipo:</span>
            <span class="field-value">${tipo}</span>
          </div>
          <div class="field">
            <span class="field-label">Protocolo:</span>
            <span class="field-value">${protocol}</span>
          </div>
          <div class="field">
            <span class="field-label">Anonimato:</span>
            <span class="field-value">${anonimo ? "Sim" : "N√£o"}</span>
          </div>
        </div>
      </div>

      <div class="section">
        <h2>Identifica√ß√£o</h2>
        <div class="field-group">
          <div class="field">
            <span class="field-label">Nome:</span>
            <span class="field-value">${anonimo ? "(an√¥nimo)" : (fields["nome"] || "")}</span>
          </div>
          <div class="field">
            <span class="field-label">CPF:</span>
            <span class="field-value">${fields["cpf"] || ""}</span>
          </div>
          <div class="field">
            <span class="field-label">E-mail:</span>
            <span class="field-value">${email || ""}</span>
          </div>
          <div class="field">
            <span class="field-label">Telefone:</span>
            <span class="field-value">${telefone || ""}</span>
          </div>
          <div class="field">
            <span class="field-label">Retorno via:</span>
            <span class="field-value">${fields["retorno"] || ""}</span>
          </div>
        </div>
      </div>

      <div class="section">
        <h2>Detalhes do Ocorrido</h2>
        <div class="field-group">
          <div class="field">
            <span class="field-label">Protocolo ref.:</span>
            <span class="field-value">${fields["protocolo"] || ""}</span>
          </div>
          <div class="field">
            <span class="field-label">Data do ocorrido:</span>
            <span class="field-value">${fields["data_ocorrido"] || ""}</span>
          </div>
          <div class="field">
            <span class="field-label">Envolvidos:</span>
            <span class="field-value">${fields["envolvidos"] || ""}</span>
          </div>
        </div>
        <h3>Relato:</h3>
        <div class="pre-text">${relato}</div>
      </div>`;

    const internalHtml = createEmailTemplate("Nova Manifesta√ß√£o de Ouvidoria", internalContent);
    const assunto = `Ouvidoria ‚Äì ${tipo} ‚Äì ${protocol}`;

    // Enviar para equipe interna
    console.log('üì§ [OUVIDORIA API] Enviando email interno...');
    console.log('üìé [OUVIDORIA API] Anexos encontrados:', attachments.length);
    
    const emailData: any = {
      from: FROM || "ouvidoria@koerner.com.br",
      to: TO,
      subject: assunto,
      html: internalHtml,
      replyTo: email || undefined,
    };
    
    // S√≥ adicionar attachments se houver arquivos v√°lidos
    if (attachments.length > 0) {
      emailData.attachments = attachments;
      console.log('üìé [OUVIDORIA API] Enviando com anexos:', attachments.map(a => a.filename));
    }
    
    const internalResult = await sendEmail(emailData);
    console.log('‚úÖ [OUVIDORIA API] Email interno enviado:', internalResult.messageId);

    // Enviar confirma√ß√£o para o usu√°rio (se n√£o for an√¥nimo e tiver email)
    if (!anonimo && email) {
      console.log('üì§ [OUVIDORIA API] Enviando confirma√ß√£o para usu√°rio...');
      const confirmationContent = createConfirmationContent('ouvidoria', email, protocol);
      const confirmationHtml = createEmailTemplate("Confirma√ß√£o - Ouvidoria", confirmationContent, true);
      
      const confirmationResult = await sendEmail({
        from: FROM || "ouvidoria@koerner.com.br",
        to: email,
        subject: `Confirma√ß√£o de Recebimento - Ouvidoria - ${protocol}`,
        html: confirmationHtml,
      });
      console.log('‚úÖ [OUVIDORIA API] Confirma√ß√£o enviada:', confirmationResult.messageId);
    } else {
      console.log('‚ÑπÔ∏è [OUVIDORIA API] Pular confirma√ß√£o (an√¥nimo ou sem email)');
    }

    return NextResponse.json({ 
      ok: true, 
      protocolo: protocol,
      data: new Date().toISOString() 
    });
  } catch (e: unknown) {
    console.error("API error:", e);
    const message = e instanceof Error ? e.message : "Erro inesperado";
    return NextResponse.json({ error: message }, { status: 500 });
  }
}